//! One-off generator for src/board/magics.rs. Run:
//!   cargo run --release --bin find_magics > src/board/magics.rs
//! Deterministic: fixed-seed SplitMix64.

use nebchess::board::attacks::{relevant_mask, sliding_attacks_slow, BISHOP_DELTAS, ROOK_DELTAS};
use nebchess::board::{Bitboard, Square};

struct SplitMix64(u64);
impl SplitMix64 {
    fn next(&mut self) -> u64 {
        self.0 = self.0.wrapping_add(0x9E3779B97F4A7C15);
        let mut z = self.0;
        z = (z ^ (z >> 30)).wrapping_mul(0xBF58476D1CE4E5B9);
        z = (z ^ (z >> 27)).wrapping_mul(0x94D049BB133111EB);
        z ^ (z >> 31)
    }
    fn sparse(&mut self) -> u64 {
        self.next() & self.next() & self.next()
    }
}

/// Enumerate all subsets of `mask` (carry-rippler), with their attack sets.
fn subsets(sq: Square, mask: Bitboard, deltas: &[(i8, i8); 4]) -> Vec<(u64, u64)> {
    let mut out = Vec::new();
    let mut sub: u64 = 0;
    loop {
        out.push((sub, sliding_attacks_slow(sq, Bitboard(sub), deltas).0));
        sub = sub.wrapping_sub(mask.0) & mask.0;
        if sub == 0 {
            break;
        }
    }
    out
}

fn find_magic(rng: &mut SplitMix64, sq: Square, deltas: &[(i8, i8); 4]) -> (u64, u64, u32) {
    let mask = relevant_mask(sq, deltas);
    let bits = mask.count();
    let shift = 64 - bits;
    let entries = subsets(sq, mask, deltas);
    let size = 1usize << bits;
    let mut table = vec![0u64; size];
    'candidate: loop {
        let magic = rng.sparse();
        // heuristic: need high bits set for the multiply-shift to spread
        if ((mask.0.wrapping_mul(magic)) >> 56).count_ones() < 6 {
            continue;
        }
        table.iter_mut().for_each(|e| *e = 0);
        let mut used = vec![false; size];
        for &(occ, attacks) in &entries {
            let idx = (occ.wrapping_mul(magic) >> shift) as usize;
            if used[idx] && table[idx] != attacks {
                continue 'candidate; // collision with different attack set
            }
            used[idx] = true;
            table[idx] = attacks;
        }
        return (mask.0, magic, shift);
    }
}

fn emit(name: &str, rng: &mut SplitMix64, deltas: &[(i8, i8); 4]) -> u32 {
    println!("#[rustfmt::skip]");
    println!("pub static {name}: [Magic; 64] = [");
    let mut offset: u32 = 0;
    for i in 0..64 {
        let sq = Square::new(i);
        let (mask, magic, shift) = find_magic(rng, sq, deltas);
        println!(
            "    Magic {{ mask: 0x{mask:016X}, magic: 0x{magic:016X}, shift: {shift}, offset: {offset} }},"
        );
        offset += 1u32 << (64 - shift);
    }
    println!("];");
    offset
}

fn main() {
    let mut rng = SplitMix64(0x4E45_4243_4845_5353); // "NEBCHESS"
    println!("//! GENERATED by `cargo run --release --bin find_magics`. Do not edit.");
    println!();
    println!("pub struct Magic {{");
    println!("    pub mask: u64,");
    println!("    pub magic: u64,");
    println!("    pub shift: u32,");
    println!("    pub offset: u32,");
    println!("}}");
    println!();
    let rook_size = emit("ROOK_MAGICS", &mut rng, &ROOK_DELTAS);
    println!();
    let bishop_size = emit("BISHOP_MAGICS", &mut rng, &BISHOP_DELTAS);
    println!();
    println!("pub const ROOK_TABLE_SIZE: usize = {rook_size};");
    println!("pub const BISHOP_TABLE_SIZE: usize = {bishop_size};");
    assert_eq!(rook_size, 102400, "plain rook magics must total 102400");
    assert_eq!(bishop_size, 5248, "plain bishop magics must total 5248");
}
